/** `mdi-account-alert-outline` -> `mdiAccountAlertOutline` (the @mdi/js export). */
function toExportName(iconName) {
  return (
    'mdi' +
    iconName
      .replace(/^mdi-/, '')
      .split('-')
      .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
      .join('')
  )
}

const seen = new Set()
const missing = []
const entries = []
for (const name of USED_ICONS) {
  if (seen.has(name)) continue
  seen.add(name)
  const exportName = toExportName(name)
  if (typeof mdi[exportName] !== 'string') {
    missing.push(`${name} -> ${exportName}`)
    continue
  }
  entries.push({ name, exportName })
}

if (missing.length > 0) {
  console.error('ERROR: no @mdi/js export for:\n  ' + missing.join('\n  '))
  process.exit(1)
}

entries.sort((a, b) => a.name.localeCompare(b.name))

const header = `// GENERATED by scripts/gen-mdi-icons.cjs — do not edit by hand.
// Regenerate with: node scripts/gen-mdi-icons.cjs
// Maps each \`mdi-*\` icon string used in the app to its @mdi/js SVG path so the
// custom Vuetify icon set can render it without shipping the full @mdi/font.
`

const importBlock =
  'import {\n' +
  entries.map((e) => `  ${e.exportName},`).join('\n') +
  "\n} from '@mdi/js'\n"

const mapBlock =
  '\nexport const iconPaths: Record<string, string> = {\n' +
  entries.map((e) => `  '${e.name}': ${e.exportName},`).join('\n') +
  '\n}\n'

const outPath = path.join(__dirname, '..', 'src', 'plugins', 'mdiIconPaths.ts')
fs.writeFileSync(outPath, header + '\n' + importBlock + mapBlock)
console.log(`Wrote ${entries.length} icon paths to ${path.relative(path.join(__dirname, '..'), outPath)}`)
