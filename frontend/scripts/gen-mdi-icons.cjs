/**
 * gen-mdi-icons.cjs
 *
 * Generates src/plugins/mdiIconPaths.ts: a map from every `mdi-*` icon string
 * used in the app to its @mdi/js SVG path constant. Tree-shaking keeps only the
 * imported paths, so we ship ~40 KB of SVG instead of the ~2.25 MB @mdi/font.
 *
 * Regenerate after adding/removing an `mdi-*` icon in templates:
 *   node scripts/gen-mdi-icons.cjs
 *
 * The USED_ICONS list below is the authoritative inventory. Keep it in sync with
 * the source (derive with: grep -rhoE "mdi-[a-z0-9]+(-[a-z0-9]+)*" src | sort -u).
 * The script fails loudly if any name has no matching @mdi/js export, so a typo
 * or a renamed icon can never silently ship a blank glyph.
 */
